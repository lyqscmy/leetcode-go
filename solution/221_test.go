package solution

import (
	"testing"
)

func TestMaximalSquare(t *testing.T) {
	tests := []struct {
		matrix [][]byte
		want   int
	}{
		{[][]byte{{0x31, 0x30, 0x31, 0x30, 0x30}, {0x31, 0x30, 0x31, 0x31, 0x31}, {0x31, 0x31, 0x31, 0x31, 0x31}, {0x31, 0x30, 0x30, 0x31, 0x30}}, 4},
		{[][]byte{{0x30, 0x31}, {0x31, 0x30}}, 1},
		{[][]byte{{0x30}}, 0},
		{[][]byte{{0x31, 0x31, 0x31, 0x31, 0x31}, {0x31, 0x31, 0x31, 0x31, 0x31}, {0x30, 0x30, 0x30, 0x30, 0x30}, {0x31, 0x31, 0x31, 0x31, 0x31}, {0x31, 0x31, 0x31, 0x31, 0x31}}, 4},
	}
	for _, tt := range tests {
		t.Run("", func(t *testing.T) {
			if got := maximalSquare(tt.matrix); got != tt.want {
				t.Errorf("maximalSquare() = %v, want %v", got, tt.want)
			}
		})
	}
}
